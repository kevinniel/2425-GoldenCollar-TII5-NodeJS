// Crée deux variables a et b (nombres)
// Calcule leur somme, leur différence et leur produit
// Affiche chaque résultat dans la console avec une phrase explicite
// Exemple : "La somme de a et b est : ..."

let a = 5;
let b = 3;
let somme = a + b;
let difference = a - b;
let produit = a * b;
console.log(`La somme de ${a} et ${b} est : ${somme}`);
console.log(`La différence de ${a} et ${b} est : ${difference}`);
console.log(`Le produit de ${a} et ${b} est : ${produit}`);